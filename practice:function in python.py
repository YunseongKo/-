# 파이썬 내장 함수의 활용
print(abs(-5)) # abs = absolute(절댓값)
print(pow(4, 2)) # 4^2 -> 16
print(max(5, 12, 37, 28)) # 최댓값 : 개수 상관없음
print(min(5,12)) # 최솟값
print(round(3.14)) # 반올림
print(round(4.98))

from math import *
print(floor(4.99)) # 내림 : floor(바닥)
print(ceil(3.14)) # 올림 : ceiling(천장)
print(sqrt(16)) # 제곱근 : 4